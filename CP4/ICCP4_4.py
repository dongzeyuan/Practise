# 从键盘输入一个整数X，规定X是一个大于10的无符号整数，且位数n不大于10位，编程求出x的第n-1位，例如输入1234，输出234

def n_1(x):
    n = 0
    while x >= 10**n :
        n += 1
    print('你输入的是一个{0}位数'.format(n))
    x1 = x % 10**(n-1)
    print('它的后{0}位数是{1}'.format((n-1), x1))

if __name__ == '__main__':
    x = int(input('请输入一个大于10小于10^9的正整数 x: '))
    n_1(x)